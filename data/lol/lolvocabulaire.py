DATA_JSON_PATH = os.path.join("data", "vocabulaire_lol.json")

def load_data():
    """Charge le vocabulaire depuis le fichier JSON."""
    with open(DATA_JSON_PATH, "r", encoding="utf-8") as f:
        return json.load(f)

