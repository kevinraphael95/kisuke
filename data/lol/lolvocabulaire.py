class VocabulaireLoL(commands.Cog):
    """
    Commande !voc â€” Consulte le vocabulaire de League of Legends
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="lolvocbulaire",
        aliases=["lolvoc"],
        help="Consulte un terme de vocabulaire de LoL.",
        description="Affiche la liste des termes de LoL et leur dÃ©finition."
    )
    async def voc(self, ctx: commands.Context, *, terme: str = None):
        """Commande principale avec menu interactif ou recherche directe."""
        try:
            data = load_data()
            if terme:
                terme = terme.lower()
                match = next((k for k in data if k.lower() == terme), None)
                if match:
                    embed = discord.Embed(
                        title=f"ğŸ“˜ Terme : {match}",
                        description=data[match],
                        color=discord.Color.green()
                    )
                    await ctx.send(embed=embed)
                else:
                    await ctx.send("âŒ Terme non trouvÃ© dans le dictionnaire de LoL.")
            else:
                view = VocabulaireSelectView(self.bot, data)
                await ctx.send("ğŸ“š Voici la liste des termes disponibles :", view=view)
        except Exception as e:
            print(f"[ERREUR voc] {e}")
            await ctx.send("âŒ Une erreur est survenue lors du chargement des donnÃ©es.")

