def afficher_course(pos):
    lignes = []
    for i, p in enumerate(pos):
        piste = EMOJI_PISTE * p
        espace = EMOJI_PISTE * (DISTANCE - p)
        lignes.append(f"Tortue {i+1} {EMOJI_TORTUES[i]} : {piste}{EMOJI_TORTUES[i]}{espace}{EMOJI_LIGNE_ARRIVEE}")
    return "\n".join(lignes)

