class CodeCommand(commands.Cog):
    """Commande !code et /code â€” Affiche un lien cliquable vers le code source"""

    def __init__(self, bot: commands.Bot):
        self.bot = bot
        self.github_url = "https://github.com/kevinraphael95/bleach-discord-bot-test"

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ”¹ Fonction interne : Envoi formatÃ©
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    async def _send_code_link(self, channel: discord.abc.Messageable):
        """Envoie un embed + bouton vers le code source."""
        try:
            # ğŸ“Œ CrÃ©ation de l'embed
            embed = discord.Embed(
                title="ğŸ“‚ Code source du bot",
                description="Voici le lien vers le dÃ©pÃ´t GitHub contenant **tout le code** du bot.",
                color=discord.Color.blurple()
            )
            embed.set_thumbnail(url="https://github.githubassets.com/images/modules/logos_page/GitHub-Mark.png")
            embed.set_footer(text="Bleach Discord Bot â€” Open Source â¤ï¸")

            # ğŸ“Œ Bouton cliquable
            view = discord.ui.View()
            view.add_item(discord.ui.Button(label="ğŸ”— Voir sur GitHub", url=self.github_url, style=discord.ButtonStyle.link))

            await safe_send(channel, embed=embed, view=view)

        except Exception as e:
            print(f"[ERREUR !code] {e}")
            await safe_send(channel, "âŒ Une erreur est survenue lors de l'envoi du lien du code.")

