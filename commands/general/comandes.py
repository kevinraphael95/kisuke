class Commandes(commands.Cog):
    """
    Commande /commandes et !commandes â€” Affiche la liste des commandes et descriptions en Markdown paginÃ©
    """
    def __init__(self, bot: commands.Bot):
        self.bot = bot

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ”¹ Fonction interne pour crÃ©er les pages Markdown
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    def build_markdown_pages(self, max_chars=1000):
        """Renvoie une liste de blocs Markdown prÃªts Ã  copier-coller dans un README.md"""
        pages = []
        current_page = ""
        categories = {}
        for cmd in self.bot.commands:
            cat = getattr(cmd, "category", "Autre")
            if cat not in categories:
                categories[cat] = []
            desc = cmd.help if cmd.help else "Pas de description."
            categories[cat].append((cmd.name, desc))

        for cat, cmds in categories.items():
            cat_text = f"### ğŸ“‚ {cat}\n\n"
            for name, desc in cmds:
                cmd_text = f"**{name}**\n{desc}\n\n"
                # Si le bloc dÃ©passe la limite, on commence une nouvelle page
                if len(current_page) + len(cat_text) + len(cmd_text) > max_chars:
                    pages.append(current_page.strip())
                    current_page = cat_text + cmd_text
                    cat_text = ""  # On ne rÃ©pÃ¨te pas le titre de catÃ©gorie sur la nouvelle page
                else:
                    current_page += cat_text + cmd_text
                    cat_text = ""
        if current_page:
            pages.append(current_page.strip())
        return pages

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ”¹ Commande SLASH
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    @app_commands.command(
        name="commandes",
        description="Affiche toutes les commandes disponibles avec leurs descriptions en Markdown (paginÃ©)."
    )
    @app_commands.checks.cooldown(1, 5.0, key=lambda i: (i.user.id))
    async def slash_commandes(self, interaction: discord.Interaction):
        try:
            pages = self.build_markdown_pages()
            response_text = "\n\n---\n\n".join(f"```md\n{p}\n```" for p in pages)
            await safe_respond(interaction, response_text)
        except app_commands.CommandOnCooldown as e:
            await safe_respond(interaction, f"â³ Attends encore {e.retry_after:.1f}s.", ephemeral=True)
        except Exception as e:
            print(f"[ERREUR /commandes] {e}")
            await safe_respond(interaction, "âŒ Une erreur est survenue.", ephemeral=True)

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
