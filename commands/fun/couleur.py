class CouleurView(discord.ui.View):
    def __init__(self, author):
        super().__init__(timeout=60)
        self.author = author

    def generer_embed(self):
        code_hex = random.randint(0, 0xFFFFFF)
        hex_str = f"#{code_hex:06X}"
        r = (code_hex >> 16) & 0xFF
        g = (code_hex >> 8) & 0xFF
        b = code_hex & 0xFF
        rgb_str = f"({r}, {g}, {b})"
        image_url = f"https://dummyimage.com/700x200/{code_hex:06x}/{code_hex:06x}.png&text=+"
        embed = discord.Embed(
            title="ğŸŒˆ Couleur alÃ©atoire",
            description=f"ğŸ”¹ **Code HEX** : `{hex_str}`\nğŸ”¸ **Code RGB** : `{rgb_str}`",
            color=code_hex
        )
        embed.set_image(url=image_url)
        return embed

    @discord.ui.button(label="ğŸ” Nouvelle couleur", style=discord.ButtonStyle.primary)
    async def regenerate(self, interaction: discord.Interaction, button: discord.ui.Button):
        if interaction.user != self.author:
            await interaction.response.send_message("âŒ Tu ne peux pas utiliser ce bouton.", ephemeral=True)
            return
        new_embed = self.generer_embed()
        await interaction.response.edit_message(embed=new_embed, view=self)

