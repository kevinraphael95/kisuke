class PartiCommand(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ›ï¸ Commande !parti : nom de parti alÃ©atoire
    # Cooldown : 1 fois toutes les 3 secondes par utilisateur
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    @commands.command(
        name="parti",
        help="ğŸ›ï¸ GÃ©nÃ¨re un nom de parti politique alÃ©atoire."
    )
    @commands.cooldown(rate=1, per=3, type=commands.BucketType.user)
    async def parti(self, ctx):
        try:
            # ğŸ“‚ Lecture du fichier contenant les morceaux de noms
            with open("data/partis_data.json", "r", encoding="utf-8") as f:
                data = json.load(f)

            premiers_mots = data.get("premiers_mots", [])
            adjectifs = data.get("adjectifs", [])
            noms = data.get("noms", [])

            # âŒ VÃ©rification des donnÃ©es prÃ©sentes
            if not (premiers_mots and adjectifs and noms):
                await ctx.send("âŒ Le fichier `partis_data.json` est incomplet.")
                return

            # ğŸ§  Construction alÃ©atoire du nom de parti
            nom_parti = f"{random.choice(premiers_mots)} {random.choice(adjectifs)} {random.choice(noms)}"

            # ğŸ“¤ Envoi du rÃ©sultat
            await ctx.send(f"ğŸ›ï¸ Voici un nom de parti politique : **{nom_parti}**")

        except FileNotFoundError:
            await ctx.send("âŒ Fichier `partis_data.json` introuvable.")
        except Exception as e:
            await ctx.send(f"âš ï¸ Une erreur est survenue : {e}")

    # âœ… Attribution Ã  la catÃ©gorie "Fun" pour `!help`
    def cog_load(self):
        self.parti.category = "Fun"

