# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â•
class FunFactCommand(commands.Cog):
    def __init__(self, bot):
        self.bot = bot

    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    # ğŸ§  Commande !funfact : donne un fait intÃ©ressant
    # Cooldown : 1 fois toutes les 3 secondes par utilisateur
    # â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    @commands.command(
        name="funfact",
        help="ğŸ§  Donne un fun fact sur Bleach Ã©crit par ChatGPT."
    )
    @commands.cooldown(rate=1, per=3, type=commands.BucketType.user)
    async def funfact(self, ctx):
        try:
            # ğŸ“‚ Lecture du fichier JSON contenant les fun facts
            with open("data/funfacts_bleach.json", "r", encoding="utf-8") as f:
                facts = json.load(f)

            if not facts:
                await ctx.send("âŒ Aucun fun fact disponible.")
                return

            # ğŸ¯ Choix alÃ©atoire dâ€™un fait
            fact = random.choice(facts)

            # ğŸ“¤ Envoi du message avec le fun fact
            await ctx.send(f"ğŸ§  **Fun Fact Bleach :** {fact}")

        # ğŸš« Gestion des erreurs
        except FileNotFoundError:
            await ctx.send("âŒ Fichier `funfacts_bleach.json` introuvable.")
        except Exception as e:
            await ctx.send(f"âš ï¸ Une erreur est survenue : {e}")

    # âœ… Assignation de catÃ©gorie automatique
    def cog_load(self):
        self.funfact.category = "Fun"

