class TopBleach(commands.Cog):
    """Commande !topbleach â€” Classement interactif de personnages Bleach"""

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="topbleach",
        help="Classe 5 personnages de Bleach Ã  l'aveugle.",
        description="Le bot te montre 5 persos de Bleach un Ã  un, Ã  classer dans ton top 5."
    )
    async def topbleach(self, ctx: commands.Context):
        try:
            persos = load_personnages_bleach()
            if not persos or len(persos) < 5:
                await safe_send(ctx.channel, "âŒ Pas assez de personnages pour lancer le jeu.")
                return

            # Filtrage des doublons par nom
            unique_persos = {p['nom']: p for p in persos}.values()
            if len(unique_persos) < 5:
                await safe_send(ctx.channel, "âŒ Pas assez de personnages uniques.")
                return

            selection = random.sample(list(unique_persos), 5)
            view = ClassementBleachView(self.bot, ctx, selection)

            embed = discord.Embed(
                title="Personnage 1 / 5",
                description=f"**{selection[0]['nom']}**",
                color=discord.Color.orange()
            )
            embed.set_footer(text="Classe ce personnage dans ton top 5.")
            await safe_send(ctx.channel, embed=embed, view=view)

        except Exception as e:
            print(f"[ERREUR topbleach] {e}")
            await safe_send(ctx.channel, "âŒ Une erreur est survenue pendant le jeu.")

