class AstucesLoL(commands.Cog):
    """
    Commande !astuce â€” Affiche une astuce LoL alÃ©atoire avec bouton pour en changer.
    """

    def __init__(self, bot: commands.Bot):
        self.bot = bot

    @commands.command(
        name="lolastuce",
        help="Affiche une astuce LoL alÃ©atoire avec un bouton pour en changer.",
        description="Ex : !astuce â€” Affiche une astuce alÃ©atoire, et tu peux cliquer sur le bouton pour une autre."
    )
    async def astuce(self, ctx: commands.Context):
        """Commande principale avec bouton interactif."""
        try:
            astuces = load_data()
            if not astuces:
                await ctx.send("âŒ Aucune astuce trouvÃ©e.")
                return

            view = AstuceView(self.bot, astuces)
            embed = discord.Embed(
                title="ğŸ’¡ Astuce League of Legends",
                description=view.current_astuce,
                color=discord.Color.gold()
            )
            await ctx.send(embed=embed, view=view)

        except Exception as e:
            print(f"[ERREUR astuce] {e}")
            await ctx.send("âŒ Une erreur est survenue lors du chargement des astuces.")

