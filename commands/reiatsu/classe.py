class ClasseSelect(discord.ui.Select):
    def __init__(self, user_id):
        self.user_id = user_id

        options = [
            discord.SelectOption(
                label=f"{data.get('Symbole', 'ðŸŒ€')} {classe}",
                description=data["Passive"][:100],
                value=classe
            )
            for classe, data in CLASSES.items()
        ]

        super().__init__(
            placeholder="Choisis ta classe Reiatsu",
            options=options,
            min_values=1,
            max_values=1
        )

    async def callback(self, interaction: discord.Interaction):
        if interaction.user.id != self.user_id:
            await safe_respond(
                interaction,
                "âŒ Tu ne peux pas choisir une classe pour un autre joueur.",
                ephemeral=True
            )
            return

        classe = self.values[0]
        user_id = str(interaction.user.id)

        try:
            # Ajuste le cooldown selon la classe choisie
            nouveau_cd = 19 if classe == "Voleur" else 24

            supabase.table("reiatsu").update({
                "classe": classe,
                "steal_cd": nouveau_cd
            }).eq("user_id", user_id).execute()

            symbole = CLASSES[classe].get("Symbole", "ðŸŒ€")

            embed = discord.Embed(
                title=f"âœ… Classe choisie : {symbole} {classe}",
                description=(
                    f"**Passive** : {CLASSES[classe]['Passive']}\n"
                    f"**Active** : {CLASSES[classe]['Active']}"
                ),
                color=discord.Color.green()
            )
            await interaction.response.edit_message(embed=embed, view=None)

        except Exception as e:
            await safe_respond(interaction, f"âŒ Erreur lors de l'enregistrement : {e}", ephemeral=True)


class ClasseSelectView(View):
    def __init__(self, user_id):
        super().__init__(timeout=60)
        self.add_item(ClasseSelect(user_id))

