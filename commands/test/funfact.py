    """Vue contenant un bouton pour afficher un autre fun fact."""
    def __init__(self, facts):
        super().__init__(timeout=60)
        self.facts = facts

    @discord.ui.button(label="ğŸ” Autre fun fact", style=discord.ButtonStyle.blurple)
    async def new_fact_button(self, interaction: discord.Interaction, button: discord.ui.Button):
        fact = random.choice(self.facts)
        await interaction.response.edit_message(content=f"ğŸ§  **Fun Fact Bleach :** {fact}", view=self)

