class PropositionModal(Modal):
    def __init__(self, parent, numbers, target):
        super().__init__(title="ğŸ§® Proposer un calcul")
        self.parent = parent
        self.numbers = numbers
        self.target = target
        self.expression = TextInput(
            label="Ton calcul (ex: (100-25)*3)",
            placeholder="Utilise uniquement les nombres affichÃ©s et + - * /",
            style=discord.TextStyle.short,
            required=True
        )
        self.add_item(self.expression)

    async def on_submit(self, interaction: discord.Interaction):
        expr = self.expression.value.replace(" ", "")
        result = safe_eval(expr)
        if result is None:
            await safe_respond(interaction, "âŒ Calcul invalide ou interdit.", ephemeral=True)
            return

        # VÃ©rifie que les nombres utilisÃ©s sont valides
        used = [int(n) for n in ''.join(c if c.isdigit() else ' ' for c in expr).split() if n]
        pool = self.numbers.copy()
        for n in used:
            if n in pool:
                pool.remove(n)
            else:
                await safe_respond(interaction, "âŒ Tu as utilisÃ© un nombre non disponible.", ephemeral=True)
                return

        diff = abs(self.target - result)
        msg = f"ğŸ§  **{interaction.user.display_name}** â†’ `{expr}` = **{result}** (diffÃ©rence : {diff})"
        if diff == 0:
            msg += "\nğŸ‰ **Le compte est bon !**"
            self.parent.stop_game = True
        await safe_send(interaction.channel, msg)
        await interaction.response.defer()

